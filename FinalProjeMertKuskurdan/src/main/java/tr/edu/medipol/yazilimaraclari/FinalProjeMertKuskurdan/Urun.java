package tr.edu.medipol.yazilimaraclari.FinalProjeMertKuskurdan;

public class Urun {
	
	public String isim;
	public String numara;
	Urun(String isim, String numara) {
		super();
		this.isim = isim;
		this.numara = numara;
	}
	String getIsim() {
		return isim;
	}
	String getNumara() {
		return numara;
	} 
	

}
